<p>正如我们所看到的，网站首页包含最新的图片和最新的日记，然后图片栏包含一些图片，有分组，点击每一个小图片还能显示大图，同时可以左右切换图片，日记中有一个列表，点击每个日记可以看到详细情况。后台系统中，可以通过下拉菜单添加图片分组，可以选择图片分组，然后上传图片，同时还可以输入标题和正文进行日记的添加。</p>
