<p>认真想了想，开始编码前还真是有不少事情要做，比如网站图标、logo、网站名字、网站风格等等，这些东西如果不设计好，没法往下做呀。</p>
<p>因为网站要展现宠物的各个方面，表达站主对宠物的喜爱，网站名称就叫宠恋秀，网站logo可以自己设计，也可以从网上找图片，我的logo设计成：</p>
<image src="papers/note/petshow/15petshow/resources/logo.jpg"/>
<p>网站风格选用天蓝底色，简约朴实风格，包括以下几个模块，首页，用于概括展示其它模块的最新内容，图片，用来集中展示站主想展示的图片，日记，用来记录站主的一些故事。</p>
<p>后台有一个登陆模块，只有管理员可以进入，进入后可以上传图片和编辑日记等等。</p>
