<p>“欣欣，动画效果虽然代码有点多，但是思路还是很清晰的，所以我很简单就实现啦！”，玩命牛总结到。</p>
<p>“嗯，那就接着往下做呀！”，欣欣不屑一顾。</p>
<p>“但是我碰到一个小问题，就是打地鼠之后，地鼠需要立即下去，而且分数还要加1，这个怎么实现我还真是不知道，css中没有变量来保存分数呀！”，玩命牛说到。</p>
<p>“嗯，这里确实又是一个难点，不过你好好想一想，之前的点击你是怎么做的？”，欣欣引导到。</p>
<p>“这个我当然记得，是用了checkbox，然后点击它会有个checked状态，用它来控制样式。”，玩命牛对答如流。</p>
<p>“嗯，看来你对之前的知识已经掌握得很好了，但是你想想，这里用checkbox行吗？”，欣欣提问到。</p>
<p>“这里用checkbox的话，你是说每个地鼠控制一个checkbox，然后点击了之后让分数发生变化？”，玩命牛猜想到。</p>
<p>“嗯，说得对，但是怎么让分数+1呢？”，欣欣提出难点。</p>
<p>“哎，头疼的就是这里呀！”，玩命牛想不到什么好办法。</p>
<p>“其实可以这样，我们不是有一个记分牌么？设置它overflow:hidden，然后，我们有一长串的数字排列着，只是在计分牌里正好显示0而已，而且上面还有一些div卡位，当点击地鼠的时候……”，欣欣提出了思路，还没说完。</p>
<p>“点击地鼠就让某个div的height变为0，这样下面的分数就会上来！这真是个好办法！”，玩命牛思维灵动，接到。</p>
<p>“嗯，不愧是我的学生，反应就是快！”，欣欣夸别人也不忘带上自己。</p>
<p>“原理我已经明白了，下面我就要去实现了。”，玩命牛迫不及待。</p>
<p>“等等，你想想，这里用checkbox会不会有什么麻烦？还有没有更好的方式呢？”，欣欣提醒到。</p>
<p>“嗯，checkbox再点击可能又会取消checked状态，这样的话如果用户双击地鼠，分数就不会变。”，玩命牛发现了问题。</p>
<p>“对头，那你有什么好办法呢？”，欣欣继续引导。</p>
<p>“这个嘛……，对了，用radio就行啊，radio选中后，不管怎么点，都是checked状态！”，玩命牛想到了。</p>
<p>“嗯，不错，去实现吧！”，欣欣说到。</p>
